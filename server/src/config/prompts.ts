/**
 * 根据语言偏好获取语言指令
 * @param language - 语言代码（'zh' 或 'en'），默认为 'zh'
 * @returns 语言指令字符串，追加到 system prompt 末尾
 */
export function getLanguageInstruction(language?: string): string {
  const lang = language?.startsWith('en') ? 'en' : 'zh';
  if (lang === 'en') {
    return '\n\n## Language\nPlease respond in English. Keep technical terms in their original form with brief explanations where needed.';
  }
  return '\n\n## 语言\n使用中文回答，专业术语保留英文原文并附中文解释。';
}
